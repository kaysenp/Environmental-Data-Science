function V = volSphere(radius)
% Calculates the volume of a sphere given its radius
% 
% volSphere.m
% Written by Kaysen Pajarillo
% 10/23/2023
%
%------------------------------------

% Input: 
%   radius, Radius of the sphere
%
% Output: 
%   V, Volume of the sphere

V = (4/3) * pi * radius^3;

end